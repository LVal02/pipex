#include "libft.h"

void	ft_free_split(void ***tab_str)
{
	int	i;

	i = 0;
	if (!(*tab_str))
		return ;
	while ((*tab_str)[i])
	{
		ft_free(&((*tab_str)[i]));
		i++;
	}
	ft_free((void **)tab_str);
	(*tab_str) = NULL;
}
