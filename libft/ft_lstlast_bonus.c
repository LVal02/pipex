#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*current;

	if (!lst)
		return (NULL);
	current = lst;
	while (lst)
	{
		current = lst;
		lst = lst->next;
	}
	return (current);
}
