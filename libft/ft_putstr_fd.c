#include "libft.h"

int	ft_putstr_fd(char *str, int fd)
{
	int	i;

	if (!*str)
		return (0);
	i = 0;
	while (str[i])
	{
		if (write(fd, &str[i], 1) == -1)
			return (-1);
		i++;
	}
	return (i);
}
